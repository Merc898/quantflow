"""Agentic intelligence endpoints.

Returns LLM-generated market intelligence reports, news sentiment,
and macro analysis.  Full analysis requires Premium subscription.
"""

from __future__ import annotations

from datetime import UTC, datetime, timedelta
from typing import Any

from fastapi import APIRouter, HTTPException, Query, status
from pydantic import BaseModel, Field
from sqlalchemy import desc, select

from quantflow.api.auth.dependencies import CurrentUser, DbDep, require_tier
from quantflow.config.constants import TIER_PREMIUM
from quantflow.config.logging import get_logger
from quantflow.db.models import AgentOutput

logger = get_logger(__name__)

router = APIRouter(tags=["intelligence"])


# ---------------------------------------------------------------------------
# Response schemas
# ---------------------------------------------------------------------------


class NewsItem(BaseModel):
    """Single news/intelligence item."""

    timestamp: datetime
    source: str
    headline: str
    sentiment: float
    url: str | None = None


class IntelligenceReport(BaseModel):
    """Full intelligence report for a symbol."""

    symbol: str
    generated_at: datetime
    overall_sentiment: float
    sentiment_regime: str
    confidence: float
    bullish_factors: list[str]
    bearish_factors: list[str]
    key_events: list[str]
    news_items: list[NewsItem]
    metadata: dict[str, Any] = Field(default_factory=dict)


class MacroReport(BaseModel):
    """Macro market intelligence report."""

    generated_at: datetime
    market_regime: str
    macro_sentiment: float
    key_themes: list[str]
    risk_factors: list[str]
    metadata: dict[str, Any] = Field(default_factory=dict)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _agent_row_to_report(row: AgentOutput, news: list[NewsItem]) -> IntelligenceReport:
    """Convert ORM AgentOutput to IntelligenceReport.

    Args:
        row: ORM AgentOutput row.
        news: Pre-fetched news items.

    Returns:
        :class:`IntelligenceReport`.
    """
    sentiment = row.sentiment_score
    if sentiment > 0.3:
        regime = "BULLISH"
    elif sentiment < -0.3:
        regime = "BEARISH"
    else:
        regime = "NEUTRAL"

    return IntelligenceReport(
        symbol=row.symbol,
        generated_at=row.time,
        overall_sentiment=round(sentiment, 4),
        sentiment_regime=regime,
        confidence=round(row.confidence, 4),
        bullish_factors=row.bullish_factors or [],
        bearish_factors=row.bearish_factors or [],
        key_events=row.key_events or [],
        news_items=news,
        metadata=row.model_metadata or {},
    )


